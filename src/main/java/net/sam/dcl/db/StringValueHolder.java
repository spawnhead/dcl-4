package net.sam.dcl.db;

/**
 * This class represents a string value holder.
 */
public final class StringValueHolder {
	public String val;

	/**
	 * Constructs an empty string value holder
	 */
	public StringValueHolder() {
	}

	/**
	 * Constructs a string value holder with a string value
	 */
	public StringValueHolder(final String _value) {
		this.val = _value;
	}
}
