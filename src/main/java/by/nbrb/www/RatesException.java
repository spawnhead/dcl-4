package by.nbrb.www;

/**
 *
 */
public class RatesException extends Exception
{
  public RatesException()
  {
    super();
  }

  public RatesException(Throwable cause)
  {
    super(cause);
  }

  public RatesException(String message, Throwable cause)
  {
    super(message, cause);
  }

  public RatesException(String message)
  {
    super(message);
  }
}